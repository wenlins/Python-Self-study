# for 变量 in [] 循环的利用
for b in[1,2,3,0,4,5,6]:
 print(b)
 print('你真浪呀')
for a in['','','','']:
 print(a)
 print('满足你')
# 这个程序执行99次
for a in range(1,100):
 print(a)
 print('我不想写作业')
# 直接写程序运行的次数 从0到99
for b in range(100):
 print(b)
# 跳跃
for c in range(0,20,2):
 print(c)
 print('赵文硕好牛逼')
a=0
for b in range(101):
 a+=b  
 print(a)
